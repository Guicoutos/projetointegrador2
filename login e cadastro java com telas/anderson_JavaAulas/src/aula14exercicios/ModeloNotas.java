package aula14exercicios;
public class ModeloNotas {
    String primeiraNota; 
    String segundaNota;
    String média;
    String situação;

    public String getPrimeiraNota() {
        return primeiraNota;
    }

    public void setPrimeiraNota(String primeiraNota) {
        this.primeiraNota = primeiraNota;
    }

    public String getSegundaNota() {
        return segundaNota;
    }

    public void setSegundaNota(String segundaNota) {
        this.segundaNota = segundaNota;
    }

    public String getMédia() {
        return média;
    }

    public void setMédia(String média) {
        this.média = média;
    }

    public String getSituação() {
        return situação;
    }

    public void setSituação(String situação) {
        this.situação = situação;
    }
    
}
